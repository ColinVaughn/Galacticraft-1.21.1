package dev.galacticraft.mod.util;

import net.minecraft.core.BlockPos;
import net.minecraft.core.Direction;
import net.minecraft.world.level.Level;
import net.neoforged.neoforge.capabilities.Capabilities;

public final class FluidUtil {
    public static final String SUFFIX_MILLIBUCKETS = "mB";
    public static final String SUFFIX_BUCKETS = "B";
    private FluidUtil() {}
    public static long bucketsToDroplets(int buckets) { return buckets * 81000L; }
    public static boolean canAccessFluid(Level level, BlockPos pos, Direction direction) {
        return Capabilities.FluidHandler.BLOCK.getCapability(level, pos, null, null, direction.getOpposite()) != null;
    }
}
